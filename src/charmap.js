var charmap = [],
  chr;

for (var i = 0; i < 256; i++) {
  if (i > 127) {
    chr = charmap_high[i - 0x80];
  } else if (i < 32) {
    chr = charmap_low[i];
  } else {
    chr = i;
  }

  charmap[i] = String.fromCharCode(chr);
}

